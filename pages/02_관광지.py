st.table(df)

# Plotly 그래프: 관광지 평점 시각화
fig = px.bar(df, x='명소', y='평점', 
             title='서울 주요 관광지 평점',
             labels={'명소': '관광지', '평점': '평균 평점'},
             color='평점', 
             color_continuous_scale='Viridis')

# 그래프 출력
st.plotly_chart(fig)
